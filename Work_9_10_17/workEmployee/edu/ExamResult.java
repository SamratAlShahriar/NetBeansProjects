
package workEmployee.edu;

public class ExamResult {
    
    private double getGPA;
    private String getGrade;

    public ExamResult(double getGPA, String getGrade) {
        this.getGPA = getGPA;
        this.getGrade = getGrade;
    }

    public double getGetGPA() {
        return getGPA;
    }

    public String getGetGrade() {
        return getGrade;
    }
    
}

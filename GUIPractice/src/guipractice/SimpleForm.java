    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        String name=txtName.getText();
        
        String email=txtEmail.getText();
        
        String message=txtMessage.getText();
        
        String gender = "";
        if(rdbMale.isSelected()){
             gender=rdbMale.getText();
        }else if(rdbFemale.isSelected()){
            gender=rdbFemale.getText();
        }else if(rdbOther.isSelected()){
            gender=rdbOther.getText();
        }
        
        String country= "";
        country=cmbCountry.getSelectedItem().toString();
        
        
        DefaultTableModel model=(DefaultTableModel)tblDisplay.getModel();
        model.addRow(new Object[] {name, email, gender, country, message});
    }//GEN-LAST:event_btnSubmitActionPerformed

